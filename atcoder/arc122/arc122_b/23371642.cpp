template <typename T>
struct cumulative_sum {
	vector<T> dat;
	cumulative_sum(int n) : dat(n + 1) {}

	void set(int k, T x) { dat[k + 1] = x; }

	void build() {
		for ( int i = 0; i < (int)dat.size(); i++ ) {
			dat[i + 1] += dat[i];
		}
	}

	// [l,r)
	T query(int l, int r) { return dat[r] - dat[l]; }
};

int main() {
	cin.tie(nullptr);
	ios::sync_with_stdio(false);

	int n;
	cin>>n;
	vector<int> a(n);
	for(auto &A:a) cin>>A;

	sort(a.begin(),a.end());
	cumulative_sum<ll> sum(n);
	rep(i,n){ sum.set(i,a[i]); }
	sum.build();


	// 目的関数(最小化したい)
	auto f = [&](double x) {
		int idx=lower_bound(a.begin(),a.end(),2*x)-a.begin();
		double sub=(n-idx)*2*x+sum.query(0,idx);
		return ((double)sum.query(0,n)+n*x-sub)/n;
	};

	// 左側
	double low = 0;
	// 右側
	double high = 1e9;

	// 500回繰り返す
	int cnt = 10000;
	while (cnt--) {
		double c1 = (low * 2 + high) / 3;
		double c2 = (low + high * 2) / 3;

		// もしf(c2)のほうが良い(小さい)なら、駄目な方lowを更新する
		if (f(c1) > f(c2)) low = c1;
		else high = c2;
	}

	cout << fixed << setprecision(20);
	cout << f(low) << endl;
	// cout<<low<<endl;
}